def sao_multiplicaveis(m1, m2):
    len1 = len(m1[0])
    len2 = len(m2)
    matriz_iguais = False
    if len1 == len2 and matriz_iguais == False:
        matriz_iguais = True
        return matriz_iguais
    else:
        return matriz_iguais
    
    
